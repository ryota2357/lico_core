pub(crate) fn generate(sh: &Shell) -> Result<()> {
    let source = fs::read_to_string(project_root().join("crates/syntax/lico.ungram"))?;
    let grammer = source.parse()?;

    let syntax_kinds = {
        let kinds = collect_syntax_kinds(&grammer);
        let source = generate_syntax_kinds(kinds);
        rustfmt(sh, source)?
    };
    let ast_nodes = {
        let nodes = collect_ast_nodes(&grammer);
        let source = generate_ast_node(nodes);
        rustfmt(sh, source)?
    };

    fn add_header(source: &str) -> String {
        [
            "//! This file is generated by `cargo xtask syntax`, do not edit by hand.",
            source,
        ]
        .join("\n")
    }
    ensure_file_contents(
        &project_root().join("crates/syntax/src/syntax_kinds.rs"),
        &add_header(&syntax_kinds),
    )?;
    ensure_file_contents(
        &project_root().join("crates/syntax/src/ast_nodes.rs"),
        &add_header(&ast_nodes),
    )?;

    Ok(())
}

